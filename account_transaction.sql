CREATE SCHEMA ACCOUNT_BANKING;


CREATE TABLE ACCOUNT_BANKING.ACCOUNT (
  account_number BIGINT not null,
  account_name varchar(50) not null,
  account_type varchar(20) not null,
  balance_date DATE not null,
  currency varchar(3) not null,
  opening_available_balance DECIMAL not null
);

CREATE TABLE ACCOUNT_BANKING.ACCOUNT_TRANSACTIONS (
  account_number BIGINT not null,
  account_name varchar(50) not null,
  value_date DATE not null,
  currency varchar(3) not null,
  debit_amt DECIMAL,
  credit_amt DECIMAL,
  debit_credit varchar(10) not null,
  transaction_narrative varchar(100)
);

ALTER TABLE ACCOUNT_BANKING.ACCOUNT_TRANSACTIONS
ADD FOREIGN KEY (account_number)
REFERENCES ACCOUNT_BANKING.ACCOUNT(account_number);

